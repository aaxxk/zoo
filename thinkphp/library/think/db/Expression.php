namespace think\db;

class Expression
{
    /**
     * Query expression
     *
     * @var string
     */
    protected $value;

    /**
     * Create a query expression
     *
     * @param  string  $value
     * @return void
     */
    public function __construct($value)
    {
        $this->value = $value;
    }

    /**
     * Get expression
     *
     * @return string
     */
    public function getValue()
    {
        return $this->value;
    }

    public function __toString()
    {
        return (string) $this->value;
    }
}
